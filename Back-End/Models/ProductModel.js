import mongoose from "mongoose";

const ProductSchema = mongoose.Schema(
  {
    status: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const ProductModel = mongoose.model("products", ProductSchema);
export default ProductModel;
